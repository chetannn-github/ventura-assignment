export const isFutureDate = (date) => {
  if(date == '-') return true;
  const [day, month, year] = date.split("/").map(Number);
  const ipoDate = new Date(year, month - 1, day); 
  const currentDate = new Date();
  return ipoDate > currentDate;
}



